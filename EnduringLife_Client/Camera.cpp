void CThirdPersonCamera::Update(XMFLOAT3& xmf3LookAt)
{
	if (m_pPlayer)
	{
		//카메라 위치 
		XMFLOAT3 xmf3Look = m_pPlayer->GetLookVector();
		m_xmf3Position.x += 0.7*xmf3Look.x;
		m_xmf3Position.z += 0.7*xmf3Look.z ; 

		auto playerPos = xmf3LookAt;
		playerPos.x += 12.f* xmf3Look.x;
		playerPos.y += 12.f*xmf3Look.y;
		playerPos.z += 12.f* xmf3Look.z;
	
		SetLookAt(playerPos);	
	}
}

void CThirdPersonCamera::SetLookAt(XMFLOAT3& xmf3LookAt)
{
	XMFLOAT4X4 mtxLookAt = Matrix4x4::LookAtLH(m_xmf3Position, xmf3LookAt, m_pPlayer->GetUpVector());
	m_xmf3Right = XMFLOAT3(mtxLookAt._11, mtxLookAt._21, mtxLookAt._31);
	m_xmf3Up = XMFLOAT3(mtxLookAt._12, mtxLookAt._22, mtxLookAt._32);
	m_xmf3Look = XMFLOAT3(mtxLookAt._13, mtxLookAt._23, mtxLookAt._33);
}
